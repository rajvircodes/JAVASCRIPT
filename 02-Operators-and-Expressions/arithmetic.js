// Arithmetic Operators in JavaScript

console.log("=============Arithmetic Operators in JavaScript===========");

let a = 10;
let b = 3;

console.log("a + b", a + b);        // Addition
console.log("a - b", a - b);        // Subtraction
console.log("a * b", a * b);        // Multiplication
console.log("a / b", a / b);        // Division
console.log("a % b", a % b);        // Modulus (remainder)
console.log("a ** b", a ** b);      // Exponention (a^b)


//Increament and Decreament 

console.log("======Increament and Decreament====");


let x = 5;
console.log("x++ =", x++); // Post-increament (use, then add 1)
console.log("After x++ =", x);

let y = 5;
console.log("++y =", ++y);  //Pre-increament (and 1, then use)




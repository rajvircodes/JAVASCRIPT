// Assignment Operators in JavaScript

let a = 10;

a += 5;   // a = a + 5
console.log("a after += 5:", a);

a -= 2;   // a = a - 2
console.log("a after -= 2:", a);

a *= 3;   // a = a * 3
console.log("a after *= 3:", a);

a /= 2;   // a = a / 2
console.log("a after /= 2:", a);

a %= 4;   // a = a % 4
console.log("a after %= 4:", a);
